
public class Position {
	
	private int fila;
	private int columna;

	public Position(int f, int c) {
		fila = f;
		columna = c;
	}

	public int getFila() {
		return fila;
	}
	
	public void setFila(int fila) {
		this.fila = fila;
	}
	
	public int getColumna() {
		return columna;
	}
	
	public void setColumna(int columna) {
		this.columna = columna;
	}
	
	
}
