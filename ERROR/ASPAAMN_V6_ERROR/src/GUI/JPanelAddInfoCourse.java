    public void addStudent(){
        try{
		clsInfoCourse objInfoCourse = new clsInfoCourse();
            
		objInfoCourse.setName(this.txtName.getText());
            objInfoCourse.setCode(this.txtCodeCourse.getText());
            objInfoCourse.setCodeTeacher(Long.parseLong(this.txtCodeTeacher.getText()));
            objInfoCourse.setAvailabity(Integer.parseInt(this.txtAvailabity.getText()));
            
		this.auxInfoCourse.AddInfoCourse(objInfoCourse);
            
		JOptionPane.showMessageDialog(this, 
				"Asignatura agregada con exito con Exito",
				"Informacion",
				JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
		}
		catch(NullPointerException e){
			JOptionPane.showMessageDialog(this,
					"Error, No hay espacio",
					"Validar datos",
					JOptionPane.ERROR_MESSAGE);
		}
		catch(NumberFormatException e){
			JOptionPane.showMessageDialog(this,
					"Error, dato mal digitado",
					"Validar datos",
					JOptionPane.ERROR_MESSAGE);
		}
		catch(Exception e){
			JOptionPane.showMessageDialog(this,
					"Error",
					"",
					JOptionPane.ERROR_MESSAGE);
		}
    }
    
    public void limpiarCampos() {
        this.txtName.setText("");
        this.txtAvailabity.setText("");
        this.txtCodeTeacher.setText("");
        this.txtCodeCourse.setText("");
    }
