    public void updateVehiculo(){
        
        String placa = this.txtPlaca.getText();
        auxVehiculo = auxVehiculoAccess.searchVehiculo(placa);
        
        String color = this.txtColor.getText();
        String caja = this.cmbTipoCaja.getSelectedItem().toString();
        String puertas = this.cmbNumPuertas.getSelectedItem().toString();
        
        auxVehiculo.setPlaca(placa);
        auxVehiculo.setColor(color);
        auxVehiculo.setCilindraje(auxVehiculo.getCilindraje());
        auxVehiculo.setCaja(caja);
        auxVehiculo.setPuertas(puertas);
        
        this.auxVehiculoAccess.updateVehiculo(this.auxVehiculo, placa);
        
        JOptionPane.showMessageDialog(this, 
                "¬°ACTUALIZADO CON EXITO!",
                "Informaci√≥n",
                JOptionPane.INFORMATION_MESSAGE);
    }
    
    public void limpiarCampos(){
        this.txtPlaca.setText("");
        this.txtColor.setText("");
        this.cmbNumPuertas.setSelectedIndex(0);
        this.cmbTipoCaja.setSelectedIndex(0);
    }
    
