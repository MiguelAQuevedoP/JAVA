     public void addStudent(){
        try{
		clsDocente objTeacher = new clsDocente();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            
		objTeacher.setName(this.txtName.getText());
            objTeacher.setDateBirth(formato.format(this.jDatebirth.getCalendar().getTime()));
            objTeacher.setId(Long.parseLong(this.txtId.getText()));
            objTeacher.setCode(Long.parseLong(this.txtCode.getText()));
            objTeacher.setEmail(this.txtEmail.getText());
            objTeacher.setPassword(this.txtPassword.getText());
            
		this.auxTeacher.AddTeacher(objTeacher);
            
		JOptionPane.showMessageDialog(this, 
				"Docente registrado con Exito",
				"Informacion",
				JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
		}
		catch(NullPointerException e){
			JOptionPane.showMessageDialog(this,
					"Error, No hay espacio",
					"Validar datos",
					JOptionPane.ERROR_MESSAGE);
		}
		catch(NumberFormatException e){
			JOptionPane.showMessageDialog(this,
					"Error, dato mal digitado",
					"Validar datos",
					JOptionPane.ERROR_MESSAGE);
		}
		catch(Exception e){
			JOptionPane.showMessageDialog(this,
					"Error",
					"",
					JOptionPane.ERROR_MESSAGE);
		}
    }
    
    public void limpiarCampos() {
        this.txtName.setText("");
        this.jDatebirth.setCalendar(null);
        this.txtCode.setText("");
        this.txtId.setText("");
        this.txtEmail.setText("");
        this.txtPassword.setText("");
    }
