    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int indice = this.lstData.getSelectedIndex();
        clsCliente c = list.get(indice);
        clsClienteDao dao = new clsClienteDao();
        dao.deleteCliente(c.getId());
        updateList();
        JOptionPane.showMessageDialog(rootPane, "se eliminó el cliente con id: "+c.getId());
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditClientActionPerformed
        int indice = this.lstData.getSelectedIndex();
        clsCliente c = list.get(indice);
        this.txtId.setText(String.valueOf(c.getId()));
        this.txtName.setText(c.getName());
        this.txtLastName.setText(c.getLastName());
        this.txtPhone.setText(c.getPhone());
        this.txtEmail.setText(c.getEmail());
        
        clsClienteDao dao = new clsClienteDao();
        
        
    }//GEN-LAST:event_btnEditClientActionPerformed

    private void updateList(){
        clsClienteDao dao = new clsClienteDao();
        list = dao.listar();
        DefaultListModel data = new DefaultListModel();
        
        for(int i=0; i <list.size(); i++){
            clsCliente c = list.get(i);
            data.addElement(c.toString());
        }
        this.lstData.setModel(data);
    }
    private void cleanSpace(){
        this.txtId.setText("");
        this.txtName.setText("");
        this.txtLastName.setText("");
        this.txtPhone.setText("");
        this.txtEmail.setText("");
    }
