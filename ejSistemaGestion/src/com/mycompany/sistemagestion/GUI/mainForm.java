    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        clsCliente c = new clsCliente();
        c.setName(this.txtName.getText());
        c.setLastName(this.txtLastName.getText());
        c.setPhone(this.txtPhone.getText());
        c.setEmail(this.txtEmail.getText());
        if(!StringUtils.isEmptyOrWhitespaceOnly(txtId.getText())){
            c.setId(Integer.parseInt(txtId.getText()));
        }
        clsClienteDao clienteDao = new clsClienteDao();
        clienteDao.saveCliente(c);
        updateList();
        cleanSpace();
    }//GEN-LAST:event_btnAddActionPerformed

